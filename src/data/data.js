export function getProductsByIds(ids = []) {
  return ids.map((id) => productMap[id]).filter(Boolean);
}

export function getProductById(productId = 'apple') {
  return productMap[productId] ?? productMap.apple;
}

export function getFeaturedProducts() {
  return getProductsByIds(featuredProductIds);
}

export function getBestSellingProducts() {
  return getProductsByIds(bestSellingProductIds);
}

export function getGroceryProducts() {
  return getProductsByIds(groceryProductIds);
}

export function getBeverages() {
  return getProductsByIds(beverageProductIds);
}

export function getSearchProducts() {
  return getProductsByIds(searchScreenProductIds);
}

export function searchProducts(query = '', filters = {}) {
  const normalizedQuery = query.trim().toLowerCase();
  const activeCategories = filters.categories ?? [];
  const activeBrands = filters.brands ?? [];

  return getSearchProducts().filter((product) => {
    const matchesQuery =
      !normalizedQuery ||
      product.name.toLowerCase().includes(normalizedQuery) ||
      product.subtitle.toLowerCase().includes(normalizedQuery) ||
      product.brand.toLowerCase().includes(normalizedQuery) ||
      product.categories.some((category) => category.toLowerCase().includes(normalizedQuery));

    const matchesCategory =
      activeCategories.length === 0 ||
      product.categories.some((category) => activeCategories.includes(category));

    const matchesBrand = activeBrands.length === 0 || activeBrands.includes(product.brand);

    return matchesQuery && matchesCategory && matchesBrand;
  });
}
